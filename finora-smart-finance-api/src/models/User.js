const mongoose = require('mongoose');
const bcrypt = require('bcryptjs');
const crypto = require('crypto');

const RefreshTokenSchema = new mongoose.Schema({
  tokenHash: { type: String, required: true },
  expiresAt: { type: Date, required: true },
  createdAt: { type: Date, default: Date.now },
  userAgent: String,
  ip: String,
});

const UserSchema = new mongoose.Schema(
  {
    // Name ist Pflicht und unique (für Login)
    name: { 
      type: String, 
      required: [true, 'Name ist erforderlich'],
      trim: true,
      minlength: [3, 'Name muss mindestens 3 Zeichen haben'],
      maxlength: [50, 'Name darf maximal 50 Zeichen haben'],
      match: [/^[a-zA-ZäöüÄÖÜß0-9\s-]+$/, 'Name darf nur Buchstaben, Zahlen, Leerzeichen und Bindestriche enthalten']
      // Index defined separately below with unique constraint
    },
    // Email ist optional (für Password-Reset)
    email: { 
      type: String, 
      required: false,
      lowercase: true, 
      trim: true,
      match: [/^[^\s@]+@[^\s@]+\.[^\s@]+$/, 'E-Mail ist ungültig']
      // Index defined separately below with sparse+unique constraint
    },
    passwordHash: { type: String, required: true },
    lastName: { type: String, default: '' },
    avatar: { type: String, default: null },
    phone: { 
      type: String, 
      default: null,
      match: [/^[\d\s+()-]+$|^$/, 'Telefonnummer hat ungültiges Format']
    },
    isVerified: { type: Boolean, default: false },
    
    // Flag: User hat bei Registration Checkbox bestätigt (kein Email = kein Reset)
    understoodNoEmailReset: { type: Boolean, default: false },

    verificationToken: String,
    verificationExpires: Date,

    passwordResetToken: String,
    passwordResetExpires: Date,

    emailChangeToken: String,
    emailChangeNewEmail: String,
    emailChangeExpires: Date,

    preferences: {
      theme: { 
        type: String, 
        default: 'system', 
        enum: ['light', 'dark', 'system']
      },
      currency: { 
        type: String, 
        default: 'EUR',
        enum: ['USD', 'EUR', 'GBP', 'CHF', 'JPY']
      },
      timezone: { 
        type: String, 
        default: 'Europe/Berlin'
      },
      language: { 
        type: String, 
        default: 'de',
        enum: ['en', 'de', 'fr', 'ar', 'ka']
      },
      dateFormat: {
        type: String,
        default: 'iso',
        enum: ['iso', 'dmy']
      },
      emailNotifications: { type: Boolean, default: true },
      notificationCategories: {
        security: { type: Boolean, default: true },
        transactions: { type: Boolean, default: true },
        reports: { type: Boolean, default: false },
        alerts: { type: Boolean, default: true },
      },
      // Budget-Einstellungen für Alerts
      budget: {
        monthlyLimit: { type: Number, default: 0 }, // 0 = kein Limit
        alertThreshold: { type: Number, default: 80, min: 0, max: 100 }, // Warnung bei X% erreicht
        categoryLimits: { type: Map, of: Number, default: {} }, // Limits pro Kategorie
        lastAlertSent: { type: Date, default: null }, // Verhindert Spam (Budget-Limit)
        lastNegativeBalanceAlert: { type: Date, default: null }, // Verhindert Spam (Negatives Saldo)
      },
    },

    lastLogin: { type: Date, default: null },
    lastPasswordChange: { type: Date, default: null },
    passwordChangedAt: { type: Date, default: null },

    refreshTokens: [RefreshTokenSchema],
  },
  { timestamps: true }
);

// Indexes
UserSchema.index({ emailChangeToken: 1 }, { sparse: true });
UserSchema.index({ lastLogin: 1 });
UserSchema.index({ name: 1 }, { unique: true });
UserSchema.index({ email: 1 }, { unique: true, sparse: true }); // sparse: erlaubt null-Werte

// Pre-save Hook: Hash password wenn neu oder geändert
UserSchema.pre('save', async function () {
  if (!this.isModified('passwordHash')) {
    return;
  }

  const salt = await bcrypt.genSalt(10);
  this.passwordHash = await bcrypt.hash(this.passwordHash, salt);
  this.lastPasswordChange = new Date();
  this.passwordChangedAt = new Date();
});

// Pre-save Hook: Validate preferences structure
UserSchema.pre('save', async function () {
  if (this.preferences) {
    const validCurrencies = ['USD', 'EUR', 'GBP', 'CHF', 'JPY'];
    const validThemes = ['light', 'dark', 'system'];
    const validLanguages = ['en', 'de', 'fr', 'ar', 'ka'];
    const validDateFormats = ['iso', 'dmy'];

    if (this.preferences.currency && !validCurrencies.includes(this.preferences.currency)) {
      throw new Error('Ungültige Währung');
    }
    if (this.preferences.theme && !validThemes.includes(this.preferences.theme)) {
      throw new Error('Ungültiges Theme');
    }
    if (this.preferences.language && !validLanguages.includes(this.preferences.language)) {
      throw new Error('Ungültige Sprache');
    }
    if (this.preferences.dateFormat && !validDateFormats.includes(this.preferences.dateFormat)) {
      throw new Error('Ungültiges Datumsformat');
    }
  }
});

// toJSON: Sensitive fields entfernen
UserSchema.methods.toJSON = function () {
  const obj = this.toObject();
  delete obj.passwordHash;
  delete obj.verificationToken;
  delete obj.verificationExpires;
  delete obj.passwordResetToken;
  delete obj.passwordResetExpires;
  delete obj.emailChangeToken;
  delete obj.__v;
  return obj;
};

// hasEmail: Prüfe ob User eine Email hat
UserSchema.methods.hasEmail = function () {
  return !!this.email && this.email.length > 0;
};

// canResetPassword: Prüfe ob User Password zurücksetzen kann
UserSchema.methods.canResetPassword = function () {
  return this.hasEmail() && this.isVerified;
};

// generateEmailAddToken: Token für nachträgliches Email-Hinzufügen
UserSchema.methods.generateEmailAddToken = function (email) {
  const token = crypto.randomBytes(32).toString('hex');
  this.emailChangeToken = crypto.createHash('sha256').update(token).digest('hex');
  this.emailChangeNewEmail = email;
  this.emailChangeExpires = new Date(Date.now() + 24 * 60 * 60 * 1000); // 24h
  return token;
};

// removeEmail: Email entfernen (mit Warnung)
UserSchema.methods.removeEmail = function () {
  this.email = null;
  this.isVerified = false;
  this.emailChangeToken = undefined;
  this.emailChangeNewEmail = undefined;
  this.emailChangeExpires = undefined;
};

// comparePassword: Verifiziere password
UserSchema.methods.comparePassword = async function (password) {
  return bcrypt.compare(password, this.passwordHash);
};

// canChangePassword: Prüfe ob Passwort gerade geändert wurde (mindestens 1h)
UserSchema.methods.canChangePassword = function () {
  if (!this.lastPasswordChange) {
    return true;
  }
  const oneHourAgo = new Date(Date.now() - 60 * 60 * 1000);
  return this.lastPasswordChange < oneHourAgo;
};

// generateEmailChangeToken: Generiere Token für Email-Change-Verifikation
UserSchema.methods.generateEmailChangeToken = function (newEmail) {
  const token = crypto.randomBytes(32).toString('hex');
  this.emailChangeToken = crypto.createHash('sha256').update(token).digest('hex');
  this.emailChangeNewEmail = newEmail;
  this.emailChangeExpires = new Date(Date.now() + 24 * 60 * 60 * 1000); // 24h
  return token;
};

// recordLogin: Update lastLogin timestamp
UserSchema.methods.recordLogin = function (meta = {}) {
  this.lastLogin = new Date();
  if (meta.userAgent || meta.ip) {
    this.addRefreshToken(crypto.randomBytes(32).toString('hex'), 7 * 24 * 3600, meta);
  }
};

// Legacy methods (kept for backward compatibility with existing code)
UserSchema.methods.setPassword = async function (password) {
  this.passwordHash = password; // Store plaintext, will be hashed by pre-save hook
  this.lastPasswordChange = new Date();
  this.passwordChangedAt = new Date();
};

UserSchema.methods.validatePassword = async function (password) {
  return bcrypt.compare(password, this.passwordHash);
};

UserSchema.methods.generateVerification = function () {
  const token = crypto.randomBytes(32).toString('hex');
  this.verificationToken = crypto.createHash('sha256').update(token).digest('hex');
  this.verificationExpires = new Date(Date.now() + 24 * 60 * 60 * 1000); // 24h
  return token;
};

UserSchema.methods.generatePasswordReset = function () {
  const token = crypto.randomBytes(32).toString('hex');
  this.passwordResetToken = crypto.createHash('sha256').update(token).digest('hex');
  this.passwordResetExpires = new Date(Date.now() + 60 * 60 * 1000); // 1h
  return token;
};

UserSchema.methods.addRefreshToken = function (token, ttlSeconds = 7 * 24 * 3600, meta = {}) {
  const tokenHash = crypto.createHash('sha256').update(token).digest('hex');
  const expiresAt = new Date(Date.now() + ttlSeconds * 1000);
  this.refreshTokens.push({ tokenHash, expiresAt, ...meta });
};

UserSchema.methods.removeRefreshToken = function (token) {
  const tokenHash = crypto.createHash('sha256').update(token).digest('hex');
  this.refreshTokens = this.refreshTokens.filter((t) => t.tokenHash !== tokenHash);
};

UserSchema.statics.findByRefreshToken = async function (token) {
  const tokenHash = crypto.createHash('sha256').update(token).digest('hex');
  return this.findOne({ 'refreshTokens.tokenHash': tokenHash });
};

module.exports = mongoose.model('User', UserSchema);
